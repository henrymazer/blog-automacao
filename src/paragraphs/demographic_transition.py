def generate_demographic_transition_paragraph() -> str:
    """Generate paragraph explaining the connection between demographic phenomena.
    
    Returns:
        Text explaining how fertility rates and aging population are connected
    """
    return (
        "Ambos os fenômenos estão interligados como parte da transição demográfica brasileira, "
        "onde as mulheres em idade fértil (15 a 49 anos) têm cada vez menos filhos, "
        "contribuindo para o envelhecimento populacional."
    )

def generate_graph_description_paragraph(state_name):
    """
    Generate description text for the population projection graph.
    
    Args:
        state_name (str): Name of the state
        
    Returns:
        str: Description text
    """
    return f"O gráfico mostra a população desde o primeiro ano que o {state_name} teve sua população contada em um Censo até a projeção de 2070."
